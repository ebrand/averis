// Get all unique categories across all products
export const getAllCategories = () => {
  const allCategories = new Set()
  mockProducts.forEach(product => {
    if (product.categorization && Array.isArray(product.categorization)) {
      product.categorization.forEach(cat => allCategories.add(cat))
    }
  })
  return Array.from(allCategories).sort()
}

// Get category statistics
export const getCategoryStats = () => {
  const categories = getAllCategories()
  return {
    total: categories.length,
    active: categories.filter(cat => {
      // Count categories that have at least one active product
      return mockProducts.some(p => 
        p.categorization && 
        p.categorization.includes(cat) && 
        p.available_flag && 
        !p.inactive_flag
      )
    }).length,
    byCategory: categories.reduce((acc, cat) => {
      acc[cat] = mockProducts.filter(p => 
        p.categorization && p.categorization.includes(cat)
      ).length
      return acc
    }, {})
  }
}

